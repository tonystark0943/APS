def addition(m, n):
    return m + n

def subtraction(m, n):
    return m - n

def multiplication(m, n):
    return m * n

def division(m, n):
    return m / n

m, n = 34, 20
print("Addition: ", addition(m, n))
print("Subtraction: ", subtraction(m, n))
print("Multiplication: ", multiplication(m, n))
print("Division: ", division(m, n))


